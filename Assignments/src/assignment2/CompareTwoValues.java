package assignment2;

public class CompareTwoValues {
	public static void main(String[] args) 
	{
	int x =25;
	int y =35;
	if (x>y)
	{
		System.out.println("x is bigger");
	}
	else if (x<y)
	{
		System.out.println("y is bigger");
	}
	else if (x==y)
	{
		System.out.println("Both are same");
	}
	else
	{
	System.out.println("Error");
	}
	}

}