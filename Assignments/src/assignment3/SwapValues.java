public class SwapValues {

	public static void main(String[] args) {
		 int x=90;
		 int y= 60;
		 x=x+y;
		 y=x-y;
		 x=x-y;
		 
		 System.out.println("x="+x);
		 System.out.println("y="+y);
		 int r=10;
		 int p= 15;
		 r=r+p;
		 p=r-p;
		 r=r-p;
		 
		 System.out.println("r="+r);
		 System.out.println("p="+p); 
		 
	}
	

}
